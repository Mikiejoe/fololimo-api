class Command(BaseCommand):
    help = 'Populate the database with agrovets from each sub-county'

    def handle(self, *args, **kwargs):
        self.populate_agrovets()



    def populate_agrovets(self):
        for i in range(len(SUB_COUNTIES)):
            name = f"Agrovet {i + 1}" 
            phone = PHONES[i]
            email = EMAILS[i]  
            location = SUB_COUNTIES[i].sub_county 
            client = Client(
                name=name,
                phone=phone,
                email=email,
                location=location,
                type='agrovet'
            )
            client.save()
            self.stdout.write(f"Created Agrovet client: {client}")